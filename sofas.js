  function appendItems(data){
    document.getElementById("div2Second").innerHTML = null;
    data.forEach(function(el,i){
       console.log(el.name1,el.price)
       let div1 = document.createElement('div');
       div1.addEventListener("click",function(){
           localStorage.setItem('viewItem',JSON.stringify(el));
       });

       let img = document.createElement("img");
       img.src = el.image;
       img.classList.add("divImg");

       let titlename1 = document.createElement('p');
       titlename1.innerText = el.name1

       let price = document.createElement("h5");
       price.innerText ="Sale Start at INR "+ el.price;

    //    div1.append(img,title,price);
       div1.append(img,price,titlename1);

       document.querySelector("#div2Second").append(div1);

    });
  }

